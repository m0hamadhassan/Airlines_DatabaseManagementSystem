BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "AirCraft" (
	"AircraftId"	INTEGER,
	"Aircraft_Number"	nVarchar(32),
	"Capacity"	INTEGER,
	"Mf_Comp"	nVarchar(32),
	"Mf_Date"	Date,
	PRIMARY KEY("AircraftId")
);
CREATE TABLE IF NOT EXISTS "Charges" (
	"ChargeId"	INTEGER,
	"Title"	nVarchar(32),
	"Amount"	INTEGER,
	"Descr"	TEXT,
	PRIMARY KEY("ChargeId")
);
CREATE TABLE IF NOT EXISTS "Country" (
	"CountryId"	INTEGER,
	"CountryName"	nVarChar(32),
	PRIMARY KEY("CountryId")
);
CREATE TABLE IF NOT EXISTS "Employee" (
	"EmpId"	INTEGER,
	"Name"	nVarChar(32),
	"Branch_id"	INTEGER,
	"Email"	nVarChar(32),
	"JobId"	nVarChar(32),
	"Tel"	nVarChar(32),
	"Hire_date"	Date,
	FOREIGN KEY("Branch_id") REFERENCES "Branch"("BranchId"),
	PRIMARY KEY("EmpId")
);
CREATE TABLE IF NOT EXISTS "Passengers" (
	"PassengerId"	INTEGER,
	"Name"	nVarChar(32),
	"Age"	INTEGER,
	"Address"	nVarChar(64),
	"Nationality"	nVarChar(32),
	"Contact_id"	INTEGER,
	FOREIGN KEY("Contact_id") REFERENCES "Contact"("ContactId"),
	PRIMARY KEY("PassengerId")
);
CREATE TABLE IF NOT EXISTS "Route" (
	"RouteId"	INTEGER,
	"Airport_Src"	nVarChar(64),
	"Destination"	nVarChar(64),
	"Route_Code"	TEXT UNIQUE,
	PRIMARY KEY("RouteId")
);
CREATE TABLE IF NOT EXISTS "State" (
	"StateId"	INTEGER,
	"StateName"	TEXT,
	"Country_id"	INTEGER,
	FOREIGN KEY("Country_id") REFERENCES "Country"("CountryId"),
	PRIMARY KEY("StateId")
);
CREATE TABLE IF NOT EXISTS "Transactions" (
	"TransId"	integer NOT NULL,
	"BookingDate"	datetime,
	"Passenger_id"	integer,
	"Flight_id"	integer,
	"Type"	bit,
	"Employee_id"	integer,
	"Charges_id"	integer,
	"Discount_id"	integer,
	"FlightPrice"	numeric,
	FOREIGN KEY("Discount_id") REFERENCES "Discounts"("DiscId"),
	FOREIGN KEY("Flight_id") REFERENCES "Flight_Schedule"("FlightId"),
	FOREIGN KEY("Passenger_id") REFERENCES "Passengers"("PassengerId"),
	FOREIGN KEY("Charges_id") REFERENCES "Charges"("ChargeId"),
	FOREIGN KEY("Employee_id") REFERENCES "Employee"("EmpId"),
	PRIMARY KEY("TransId")
);
CREATE TABLE IF NOT EXISTS "Discounts" (
	"DiscId"	INTEGER,
	"Title"	nVarChar(32),
	"Amount"	TEXT,
	PRIMARY KEY("DiscId")
);
CREATE TABLE IF NOT EXISTS "Flight_Schedule" (
	"FlightId"	integer NOT NULL,
	"Flight_Date"	datetime,
	"Departure"	nvarchar(52) COLLATE NOCASE,
	"Arrival"	nvarchar(52) COLLATE NOCASE,
	"AirCraft_id"	integer,
	"Route_id"	integer,
	FOREIGN KEY("Route_id") REFERENCES "Route"("RouteId"),
	FOREIGN KEY("AirCraft_id") REFERENCES "AirCraft"("AircraftId"),
	PRIMARY KEY("FlightId")
);
CREATE TABLE IF NOT EXISTS "Branch" (
	"BranchId"	INTEGER,
	"Center"	nVarchar(32),
	"State_id"	INTEGER,
	FOREIGN KEY("State_id") REFERENCES "State"("StateId"),
	PRIMARY KEY("BranchId")
);
CREATE TABLE IF NOT EXISTS "Contact" (
	"ContactId"	INTEGER,
	"Email"	nVarchar(64),
	"CellPhone"	nVarchar(64),
	"State_id"	INTEGER,
	FOREIGN KEY("State_id") REFERENCES "State"("StateId"),
	PRIMARY KEY("ContactId")
);
